    public Maze() {
        walls = new ArrayList<>();
        createMaze();
    }

    private void createMaze() {
        for (int row = 0; row < MAZE_LAYOUT.length; row++) {
            for (int col = 0; col < MAZE_LAYOUT[row].length; col++) {
                if (MAZE_LAYOUT[row][col] == 1) {
                    walls.add(new Wall(col * Wall.getSize(), (MAZE_LAYOUT.length - row - 1) * Wall.getSize()));
                }
            }
        }
    }

    public int[][] getMazeLayout() {
        return MAZE_LAYOUT;
    }
    public List<Wall> getWalls() {
        return walls;
    }
